public class Condominio {
    
    private int id_condomy;
    private int cnpj;
    private String name;
    private String endereco;
    private String sindico;
    private int telefone;
    private int NumApartamento;
    private int cpf;
    private int rg;
    private String NomeBanco;
    private int agencia;
    private int conta;

    public int getId_condomy() {
        return id_condomy;
    }

    public void setId_condomy(int id_condomy) {
        this.id_condomy = id_condomy;
    }

    public int getCnpj() {
        return cnpj;
    }

    public void setCnpj(int cnpj) {
        this.cnpj = cnpj;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getSindico() {
        return sindico;
    }

    public void setSindico(String sindico) {
        this.sindico = sindico;
    }

    public int getTelefone() {
        return telefone;
    }

    public void setTelefone(int telefone) {
        this.telefone = telefone;
    }

    public int getNumApartamento() {
        return NumApartamento;
    }

    public void setNumApartamento(int NumApartamento) {
        this.NumApartamento = NumApartamento;
    }

    public int getCpf() {
        return cpf;
    }

    public void setCpf(int cpf) {
        this.cpf = cpf;
    }

    public int getRg() {
        return rg;
    }

    public void setRg(int rg) {
        this.rg = rg;
    }

    public String getNomeBanco() {
        return NomeBanco;
    }

    public void setNomeBanco(String NomeBanco) {
        this.NomeBanco = NomeBanco;
    }

    public int getAgencia() {
        return agencia;
    }

    public void setAgencia(int agencia) {
        this.agencia = agencia;
    }

    public int getConta() {
        return conta;
    }

    public void setConta(int conta) {
        this.conta = conta;
    }
    
    
}
